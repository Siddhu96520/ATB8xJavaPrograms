package Sept;

public class DataTypes_03 {

    public static void main(String[] args){

        //Rules to create variable name

        // **Variable Name (identifier)**

        // **Rules to follow when you define an Identifier:**

        //1. Identifier can contain Alphabets, Digits, and two special symbol i.e. Dollar ($),
        //2. Underscore (_).
        //3. First character of an identifier must be an Alphabet or Dollar ($) or Underscore (_).
        //4. Keywords or Reserved words can't be used as Identifiers.


        byte b=127;  //8bits, 1 Byte
        short s=128; //16bits, 2 Bytes
        int i=1000; //32bits, 4 Bytes
        long phone =965205; // 64 bits 8 Bytes

        char grade = 'A';  //--> ASCII value -int - Assigned 16 bits 2 Bytes

        float f = 3.15f; // 32 Bits 4 Bytes

        double d = 118.678; // 64 Bites, 8 Bytes



    }
}
