package Sept;

public class Sconcadination {

    public static void main(String[] args){

        String first_name= "Siddeswara";
        String last_name="Urimella";

        int a=10;
        int b=20;

        System.out.println(first_name + last_name + a + b); // Whenever first(Previous) one is string + operator work as concadination
        System.out.println(a + b + first_name + last_name);// Whenever first one is int first two will be mathametic and remaining concadinnation
        System.out.println(first_name + last_name + (a + b));

    }
}
